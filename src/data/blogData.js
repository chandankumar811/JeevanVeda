<li><strong>अमलको रस:</strong> बिहान खाली पेटमा अमलाको रस पिउनु सबैभन्दा फाइदाजनक मानिन्छ। थोरै पानी र मह मिसाउँदा यसको स्वाद राम्रो हुन्छ।</li>
